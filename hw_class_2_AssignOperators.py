#---------------------------Python Assignment Operators---------------------

'''
Operator 	Example 	Same As 	
= 	        x = 5 	    x = 5 	
+= 	        x += 3 	    x = x + 3 	
-= 	        x -= 3 	    x = x - 3 	
*= 	        x *= 3 	    x = x * 3 	
/= 	        x /= 3 	    x = x / 3 	
%= 	        x %= 3 	    x = x % 3 	
//= 	    x //= 3 	x = x // 3 	
**= 	    x **= 3 	x = x ** 3 	
&= 	        x &= 3 	    x = x & 3 	
|= 	        x |= 3 	    x = x | 3 	
^= 	        x ^= 3 	    x = x ^ 3 	
>>= 	    x >>= 3 	x = x >> 3 	
<<= 	    x <<= 3 	x = x << 3
'''


#-------------------------=------------
my_var=10
print(my_var)
#10

#-------------------------  +=  ------------
my_var+=10
print(my_var)
#20

#-------------------------  -=  ------------
my_var-=2
print(my_var)
#18

#-------------------------  *=  ------------
my_var*=2
print(my_var)
#36

#-------------------------  /=  ------------
my_var/=4
print(my_var)
#9.0

#-------------------------  %=  ------------
my_var%=5
print(my_var)
#4.0

#-------------------------  **=  ------------
my_var**=5
print(my_var)
#1024.0


#---------------------------End of Python Assignment Operators---------------------
